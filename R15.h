forward(YREAL YS1R4,YREAL PER1){ 	
    
	YS2= PER1*YS1R4;
	
	if(YS1R4-YS2<0.0001)
		YS1=0.0001;
	else
		YS1= YS1R4-YS2;
}

//===========================================================
backward(YREAL YS1R4,YREAL PER1){
	
	YJ2I1 = PER1;
	YJ2I2 = YS1R4;

	if(YS1R4-YS2<0.0001){
		YJ1I1 = 0;
		YJ1I2 = 0;	
	}else{
		YJ1I1 = 1-PER1;
		YJ1I2 = -YS1R4;	
	}
	
}
  
